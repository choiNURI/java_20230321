package day02;

public class 별찍기 {

	public static void main(String[] args) {

		
		
		
		for(int i =1 ; i<=10 ; i++) {
			for(int j=0 ; j<i ; j++) {
				System.out.print("*");
			}
			System.out.println();
		}		
			for(int k =1 ; k<=10 ; k++) {
				for(int y=9 ; y>=k ; y--) {
					System.out.print("*");
				}
				System.out.println();
					
			}
		}
	
		}
		
	

	
